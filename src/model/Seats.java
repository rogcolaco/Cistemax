package model;

public class Seats {

    private int number;
    private boolean availability;

    public Seats() {
        this.number = number;
        this.availability = availability;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public boolean isAvailability() {
        return availability;
    }

    public void setAvailability(boolean availability) {
        this.availability = availability;
    }

}
